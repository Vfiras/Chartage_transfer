class Vehicle {
  final int id;
  final String name;
  final String model;
  final String capacity;
  final int bags;
  final String label;
  final bool labelGold;
  final int price;
  final String image;

  const Vehicle({
    required this.id,
    required this.name,
    required this.model,
    required this.capacity,
    required this.bags,
    required this.label,
    required this.labelGold,
    required this.price,
    required this.image,
  });
}
